package algorithm;

import java.util.*;

public class Lv01_1000 {

	public static void main(String[] args) {
		// 두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성하시오.
		Scanner sc = new Scanner(System.in);
		System.out.println("두 수를 입력하세요:");
		int a = sc.nextInt();
		int b = sc.nextInt();
		int c = a+b;
		System.out.println("두 수의 합은 "+c+"입니다.");
		
	}

}
